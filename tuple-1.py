# tuple data structure----
number = (12, 23, 45, 65, 78, 65, 31, 55)
print(number)
print(type(number))  # ans : tuple

# tuple data structure----
x = 23, 56, 67, 35, 90
print(x)
print(type(x))  # ans : tuple

# not a tuple data structure----
x = 1
print(x)
print(type(x))  # ans : 'int'

# tuple data structure----
x = 1,
print(x)
print(type(x))  # ans : 'tuple'


# not a tuple data structure----
x = (1)
print(x)
print(type(x))     # ans : 'int'

# tuple data structure----
x = (1,)
print(x)
print(type(x)) # ans : 'tuple'

# empty tuple data structure----
x = ()
print(x)
print(type(x)) # ans : 'tuple'